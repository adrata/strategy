  /**
   * Get competitor-specific profile by name
   */
  static getCompetitorProfile(competitorName: string): string {
    const normalized = competitorName.toLowerCase().trim();
    
    if (normalized.includes('burns') || normalized.includes('mcdonnell') || normalized === 'b&m' || normalized === 'b&m') {
      return this.getBurnsMcDonnellProfile();
    }
    
    if (normalized.includes('black') || normalized.includes('veatch') || normalized === 'b&v' || normalized === 'b&v') {
      return this.getBlackVeatchProfile();
    }
    
    if (normalized.includes('lockard') || normalized.includes('white') || normalized === 'l&w' || normalized === 'l&w') {
      return this.getLockardWhiteProfile();
    }
    
    // Default to positioning playbook if competitor unknown
    return this.getPositioningPlaybook();
  }

  /**
   * Get contextual manual based on query or situation
   */
  static getContextualManual(context: {
    competitorName?: string;
    situation?: string;
    queryType?: string;
  }): string {
    const { competitorName, situation, queryType } = context;
    
    let manual = '';
    
    // If specific competitor mentioned, include their profile
    if (competitorName) {
      manual += this.getCompetitorProfile(competitorName) + '\n\n';
    }
    
    // Add positioning playbook for general competitive situations
    if (situation === 'competitive' || queryType === 'positioning' || !competitorName) {
      manual += this.getPositioningPlaybook() + '\n\n';
    }
    
    // Always include cheat sheet for quick reference
    manual += this.getSalesCheatSheet();
    
    return manual;
  }
