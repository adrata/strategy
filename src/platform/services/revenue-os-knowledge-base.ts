  /**
   * Get framework context based on record type and situation
   */
  static getContextualFramework(context: {
    recordType?: string;
    status?: string;
    situation?: string;
  }): string {
    const { recordType, status, situation } = context;

    // Determine which framework(s) to include
    let framework = '';

    // Acquisition context
    if (
      recordType === 'lead' || 
      recordType === 'prospect' || 
      status === 'LEAD' || 
      status === 'PROSPECT' ||
      situation === 'acquisition'
    ) {
      framework += this.getAcquisitionFramework();
    }

    // Retention context
    if (
      recordType === 'customer' || 
      status === 'CUSTOMER' ||
      situation === 'retention'
    ) {
      framework += this.getRetentionFramework();
    }

    // Expansion context
    if (
      situation === 'expansion' ||
      situation === 'upsell' ||
      situation === 'cross-sell'
    ) {
      framework += this.getExpansionFramework();
    }

    // If no specific context, provide all frameworks
    if (!framework) {
      framework = this.getCompleteFramework();
    }

    return framework;
  }
