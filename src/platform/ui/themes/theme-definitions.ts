// ==================== THEME UTILITIES ====================

export const getThemeById = (id: string): Theme | undefined => {
  return allThemes.find(theme => theme.id === id);
};

export const getThemesByCategory = (category: Theme['category']): Theme[] => {
  return allThemes.filter(theme => theme.category === category);
};

export const searchThemes = (query: string): Theme[] => {
  const lowercaseQuery = query.toLowerCase();
  return allThemes.filter(theme => 
    theme.name.toLowerCase().includes(lowercaseQuery) ||
    theme.displayName.toLowerCase().includes(lowercaseQuery) ||
    theme.metadata.description.toLowerCase().includes(lowercaseQuery) ||
    theme.metadata.tags.some(tag => tag.toLowerCase().includes(lowercaseQuery))
  );
};

export const getPopularThemes = (limit: number = 10): Theme[] => {
  return allThemes
    .sort((a, b) => b.metadata.popularityScore - a.metadata.popularityScore)
    .slice(0, limit);
};

export const getAccessibleThemes = (level: 'AAA' | 'AA' | 'A' = 'AA'): Theme[] => {
  const levels = ['A', 'AA', 'AAA'];
  const targetLevelIndex = levels.indexOf(level);
  return allThemes.filter(theme => {
    const themeLevelIndex = levels.indexOf(theme.metadata.accessibility);
    return themeLevelIndex >= targetLevelIndex;
  });
};

