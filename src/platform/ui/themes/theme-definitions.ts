export interface ThemeColors {
  // Core colors
  background: string;
  foreground: string;
  accent: string;
  border: string;
  muted: string;
  mutedLight: string;
  loadingBg: string;
  hover: string;
  
  // Interactive elements
  buttonText: string;
  buttonBackground: string;
  buttonHover: string;
  buttonActive: string;
  
  // Status colors
  success: string;
  warning: string;
  error: string;
  info: string;
  
  // Badge colors
  badgeNewBg: string;
  badgeNewText: string;
  badgeContactedBg: string;
  badgeContactedText: string;
  badgeQualifiedBg: string;
  badgeQualifiedText: string;
  badgeLostBg: string;
  badgeLostText: string;
  
  // Special elements
  activeAppBorder: string;
  panelBackground: string;
  scrollbarThumb: string;
  
  // Focus and accessibility
  focusRing: string;
  focusRingWidth: string;
  highContrastBg: string;
  highContrastFg: string;
  highContrastBorder: string;
  highContrastAccent: string;
  
  // Modal and overlay
  overlayBg: string;
  overlayOpacity: string;
  
  // Validation states
  successBg: string;
  successText: string;
  successBorder: string;
  warningBg: string;
  warningText: string;
  warningBorder: string;
  errorBg: string;
  errorText: string;
  errorBorder: string;
  infoBg: string;
  infoText: string;
  infoBorder: string;
  
  // Status semantic colors
  statusNewBg: string;
  statusNewText: string;
  statusContactedBg: string;
  statusContactedText: string;
  statusQualifiedBg: string;
  statusQualifiedText: string;
  statusWonBg: string;
  statusWonText: string;
  statusLostBg: string;
  statusLostText: string;
  
  // Priority colors
  priorityHighBg: string;
  priorityHighText: string;
  priorityMediumBg: string;
  priorityMediumText: string;
  priorityLowBg: string;
  priorityLowText: string;
}

export interface ThemeMetadata {
  contrastRatio: number;
  accessibility: 'AAA' | 'AA' | 'A';
  popularityScore: number;
  description: string;
  tags: string[];
  author?: string;
  source?: string;
}

export interface Theme {
  id: string;
  name: string;
  displayName: string;
  category: 'light' | 'dark' | 'high-contrast';
  colors: ThemeColors;
  metadata: ThemeMetadata;
}

